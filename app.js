function rozpocznijTest(idTestu) {
    aktualnyTest = bazaTestow.find(t => t.id === idTestu);
    indeksPytania = 0;
    punkty = 0;
    
    ukryjWszystkieWidoki();
    widokRozwiazywania.classList.add('aktywny');
    pokazPytanie();
}

function pokazPytanie() {
    const pytanie = aktualnyTest.pytania[indeksPytania];
    testPostep.innerText = `Pytanie ${indeksPytania + 1} z ${aktualnyTest.pytania.length}`;
    testPytanie.innerText = pytanie.tresc;
    pojemnikOdpowiedzi.innerHTML = ''; // Czyścimy poprzednie odpowiedzi

    if (pytanie.typ === 'zamkniete') {
        pytanie.odpowiedzi.forEach((odpowiedz, index) => {
            const btn = document.createElement('button');
            btn.className = 'przycisk-odpowiedz';
            btn.innerText = odpowiedz;
            btn.addEventListener('click', () => sprawdzOdpowiedzZamknieta(index));
            pojemnikOdpowiedzi.appendChild(btn);
        });
    } else if (pytanie.typ === 'otwarte') {
        const input = document.createElement('input');
        input.type = 'text';
        input.className = 'pole-tekstowe';
        input.placeholder = 'Wpisz odpowiedź...';
        
        const btnSprawdz = document.createElement('button');
        btnSprawdz.className = 'przycisk-sprawdz';
        btnSprawdz.innerText = 'Sprawdź';
        btnSprawdz.addEventListener('click', () => sprawdzOdpowiedzOtwarta(input.value));

        pojemnikOdpowiedzi.appendChild(input);
        pojemnikOdpowiedzi.appendChild(btnSprawdz);
    }
}

function sprawdzOdpowiedzZamknieta(wybranyIndeks) {
    const pytanie = aktualnyTest.pytania[indeksPytania];
    if (wybranyIndeks === pytanie.poprawna) punkty++;
    nastepnePytanie();
}

function sprawdzOdpowiedzOtwarta(wpisanyTekst) {
    const pytanie = aktualnyTest.pytania[indeksPytania];
    // Zmieniamy wpisany tekst na małe litery i usuwamy spacje z boków, żeby uniknąć głupich błędów
    const sformatowanaOdpowiedz = wpisanyTekst.trim().toLowerCase();
    if (sformatowanaOdpowiedz === pytanie.poprawna) punkty++;
    nastepnePytanie();
}

function nastepnePytanie() {
    indeksPytania++;
    if (indeksPytania < aktualnyTest.pytania.length) {
        pokazPytanie();
    } else {
        pokazWynik();
    }
}

function pokazWynik() {
    ukryjWszystkieWidoki();
    widokWyniku.classList.add('aktywny');
    wynikLiczbowy.innerText = `${punkty} / ${aktualnyTest.pytania.length}`;
}

